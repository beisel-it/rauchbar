import { mkdir, readFile, rm, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath } from "node:url";
import { screenshotCaptures } from "./capture-config.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..", "..");
const outputDir = path.join(rootDir, "artifacts", "readme-screenshots");
const generatedDocsDir = path.join(rootDir, "docs", "ux", "generated");
const manifestPath = path.join(outputDir, "manifest.json");
const catalogPath = path.join(generatedDocsDir, "readme-screenshot-catalog.md");
const manifestOnly = process.argv.includes("--manifest-only");

function runCommand(command, args) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: rootDir,
      stdio: ["ignore", "pipe", "pipe"],
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });

    child.stderr.on("data", (chunk) => {
      stderr += chunk.toString();
    });

    child.on("exit", (code) => {
      if (code === 0) {
        resolve(stdout.trim());
        return;
      }

      reject(new Error(`${command} ${args.join(" ")} failed with code ${code}: ${stderr}`));
    });
  });
}

async function readPackageVersion() {
  const packageJson = JSON.parse(await readFile(path.join(rootDir, "package.json"), "utf8"));
  return packageJson.version;
}

function parseSize(size) {
  const [width, height] = size.split(",").map(Number);
  return { width, height };
}

async function buildManifest() {
  const packageVersion = await readPackageVersion();
  const gitRevision = await runCommand("git", ["rev-parse", "HEAD"]);
  const gitBranch = await runCommand("git", ["branch", "--show-current"]);
  const generatedAt = new Date().toISOString();

  const assets = [];

  for (const capture of screenshotCaptures) {
    const filePath = path.join(outputDir, capture.file);
    const fileStats = await stat(filePath);
    assets.push({
      id: capture.id,
      file: capture.file,
      route: capture.route,
      surface: capture.surface,
      priority: capture.priority,
      title: capture.title,
      purpose: capture.purpose,
      viewport: parseSize(capture.size),
      bytes: fileStats.size,
    });
  }

  return {
    generatedAt,
    packageVersion,
    gitRevision,
    gitBranch,
    assetCount: assets.length,
    assets,
  };
}

function renderCatalog(manifest) {
  const lines = [
    "# Generated README Screenshot Catalog",
    "",
    "This file is generated by `pnpm ux:docs:refresh`.",
    "",
    `- Package version: \`${manifest.packageVersion}\``,
    `- Git revision: \`${manifest.gitRevision}\``,
    `- Git branch: \`${manifest.gitBranch}\``,
    `- Generated at: \`${manifest.generatedAt}\``,
    `- Asset count: \`${manifest.assetCount}\``,
    "",
    "## Assets",
    "",
  ];

  for (const asset of manifest.assets) {
    lines.push(`### ${asset.title}`);
    lines.push("");
    lines.push(`- ID: \`${asset.id}\``);
    lines.push(`- Surface: \`${asset.surface}\``);
    lines.push(`- Priority: \`${asset.priority}\``);
    lines.push(`- File: \`${path.posix.join("artifacts/readme-screenshots", asset.file)}\``);
    lines.push(`- Route: \`${asset.route}\``);
    lines.push(`- Viewport: \`${asset.viewport.width}x${asset.viewport.height}\``);
    lines.push(`- Bytes: \`${asset.bytes}\``);
    lines.push(`- Purpose: ${asset.purpose}`);
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

if (!manifestOnly) {
  await runCommand(process.execPath, [path.join(__dirname, "capture-screenshots.mjs")]);
}

await rm(generatedDocsDir, { recursive: true, force: true });
await mkdir(generatedDocsDir, { recursive: true });
await mkdir(outputDir, { recursive: true });

const manifest = await buildManifest();

await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
await writeFile(catalogPath, renderCatalog(manifest));

console.log(`Wrote ${path.relative(rootDir, manifestPath)}`);
console.log(`Wrote ${path.relative(rootDir, catalogPath)}`);
